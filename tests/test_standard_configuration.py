import pytest
from ._helpers import create_test_namelist_dict, ROMSConfiguration, ROMS_ROOT, get_summary_value


@pytest.fixture()
def standard_configuration(tmp_path_factory, read_cppdefs) -> ROMSConfiguration:
    """Generates a session-scoped ROMSConfiguration using the default cppkey set"""

    cpp_keys=read_cppdefs(ROMS_ROOT/"src/cppdefs.opt")
    build_dir = tmp_path_factory.mktemp("standard_configuration_build")
    conf = ROMSConfiguration(cpp_keys=cpp_keys, location=build_dir)
    conf.compile()
    return conf

@pytest.fixture()
def read_cppdefs():
    """Simple fixture to read a cppdefs.opt file from a ROMS configuration.

    Requires that each key starts with 'define' and that the first character is not a !
    """
    def _read_cppdefs(fname):
        cppkeys = []
        with open(fname,"r") as cppfile:
            cpplines = cppfile.readlines()
        for line in cpplines:
            thisline = line.lower().strip()
            if ("define" in thisline) and (thisline[0]!="!"):
                thiskey = thisline.split("define")[-1].split("!")[0]
                cppkeys.append(thiskey.strip().upper())
        return cppkeys
    return _read_cppdefs


